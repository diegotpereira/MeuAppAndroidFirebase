package br.java.meuappandroidfirebase;

public class DadosUsuario {

    public String endereco;
    public String idade;
    public String nome;
    public String fone;
    public String cep;

    public DadosUsuario(String endereco, String idade, String nome, String fone, String cep) {
        this.endereco = endereco;
        this.idade = idade;
        this.nome = nome;
        this.fone = fone;
        this.cep = cep;
    }
}
